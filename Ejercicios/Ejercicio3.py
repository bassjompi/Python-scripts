#!/usr/bin/python

num1 = input ('Introduzca el primer numero: ')
num2 = input ('Introduzca el segundo numero, hijo de puta: ')

if num1 == num2:
        print ('Los numeros son iguales')

elif num1>num2:
        print ('%s es mayor que %s' % (num1, num2))

else:
        print ('%s es mayor que %s' % (num2, num1))
